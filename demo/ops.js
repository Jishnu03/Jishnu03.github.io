//Arithmetic Operators

// +
// -
// *
// /
// %
var x=13;
var y=20;

console.log(x+y);//33
console.log(x-y);//-7
console.log(x*y);//260
console.log(x/y);//0.65
console.log(x%y);//13
console.log(y%x);//7

console.log(++x);//14
console.log(y++);//20
console.log(y);//21

console.log(--x);//13
console.log(y--);//21
console.log(y);//20

var z=10;
z+=2;//equivalent to  z=z+2;
console.log(z);